class Class_Potential_Origin
{
public:
  Class_Potential_Origin ();
  virtual   ~Class_Potential_Origin ();

  /**
   * Number of Lagrange parameters in the Higgs Tree-Level potential
   */
  int nPar = 0;
  /**
     * Number of Lagrange parameters in the Higgs Counterterm potential
     */
  int nParCT=0;
  /**
   * MSBar renormalization scale
   */
  double scale = C_vev0;
  /**
   * Parameter to differentiate the models
   */
  int Model = -1;
  /**
   * Number of neutral Higgs bosons
   */
  int NNeutralHiggs = 0;
  /**
   * Number of charged Higgs bosons
   */
  int NChargedHiggs = 0;
  /**
   * Number of all Higgs particles. This will define the size of your Higgs mass matrix.
   */
  int NHiggs = NNeutralHiggs+NChargedHiggs;
  /**
   * Number of gauge bosons. Do not change this in the current version as we only investigate extended Higgs
   * sectors. If you want to extend the other sectors as well the Debye corrections have to be calculated by hand.
   */
  int NGauge = 4;
  /**
     * Number of quarks. Do not change this in the current version as we only investigate extended Higgs
     * sectors. If you want to extend the other sectors as well the Debye corrections have to be calculated by hand.
     */
  int NQuarks = 12;

  int NColour = 3;

  /**
     * Number of leptons. Do not change this in the current version as we only investigate extended Higgs
     * sectors. If you want to extend the other sectors as well the Debye corrections have to be calculated by hand.
     */


  int NLepton = 9;

  /**
   * Number of VEVs you want to minimize.
   */
  int nVEV=0;

  /**
     * Storage of the symmetric VEV in all Higgs fields
     */
    std::vector<double> VEVSymmetric;
  /**
   * Storage of the tree-level VEV in all Higgs fields
   */
  std::vector<double> vevTree;
  /**
   * Storage of the tree-Level VEV in the configuration for the minimizer
   */
  std::vector<double> vevTreeMin;
  /**
   * Storage of the contributions of the Coleman-Weinberg potential to the triple Higgs couplings in the
   * gauge basis
   */
  std::vector<double> TripleHiggsCorrectionsCW;
  /**
    * Storage of the contributions of the Coleman-Weinberg potential to the triple Higgs couplings in the
    * mass basis
    */
  std::vector<std::vector<std::vector< double>>> TripleHiggsCorrectionsCWPhysical;
  /**
     * Storage of the contributions of the tree-level potential to the triple Higgs couplings in the
     * mass basis
     */
   std::vector<std::vector<std::vector< double>>> TripleHiggsCorrectionsTreePhysical;
   /**
      * Storage of the contributions of the counterterm potential to the triple Higgs couplings in the
      * mass basis
      */
    std::vector<std::vector<std::vector< double>>> TripleHiggsCorrectionsCTPhysical;

  int nTripleCouplings=0;

  bool SetCurvatureDone = false;
  bool CalcCouplingsdone = false;
  bool CalculatedTripleCopulings = false;


  /**
   * Storage of the Tree-Level Higgs VEV
   */
  std::vector<double> HiggsVev;
  /**
   * L_{(S)}^{i}
   */
  std::vector<double> Curvature_Higgs_L1;
  /**
     * L_{(S)}^{ij
     */
  std::vector< std::vector<double> > Curvature_Higgs_L2;
  /**
     * L_{(S)}^{ijk}
     */
  std::vector< std::vector< std::vector<double> > > Curvature_Higgs_L3;
  /**
     * L_{(S)}^{ijkl}
     */
  std::vector< std::vector< std::vector< std::vector<double> > > >Curvature_Higgs_L4;
  /**
     * L_{(S),CT}^{i}
     */
   std::vector<double> Curvature_Higgs_CT_L1;
   /**
        * L_{(S),CT}^{ij}
        */
  std::vector< std::vector<double> > Curvature_Higgs_CT_L2;
  /**
       * L_{(S),CT}^{ijk}
       */
  std::vector< std::vector< std::vector<double> > > Curvature_Higgs_CT_L3;
  /**
       * L_{(S),CT}^{ijkl}
       */
  std::vector< std::vector< std::vector< std::vector<double> > > >Curvature_Higgs_CT_L4;
  /**
   * G^{abij}
   */
  std::vector< std::vector< std::vector< std::vector<double> > > >Curvature_Gauge_G2H2;
  /**
   * Y^{IJk} for Quarks
   */
  std::vector< std::vector< std::vector< std::complex<double>> > > Curvature_Quark_F2H1;
  /**
   * Y^{IJk} for Leptons
   */
  std::vector< std::vector< std::vector< std::complex<double>> > > Curvature_Lepton_F2H1;
  std::vector<double> MassSquaredHiggs,MassSquaredGauge,MassSquaredQuark,MassSquaredLepton;
  /**
   * Storage of the Higgs rotation matrix for the Higgs mass matrix at the tree-level Vacuum
   */
  std::vector< std::vector<double> > HiggsRotationMatrix;
  std::vector< std::vector< std::vector< std::vector<double>>>> Couplings_Higgs_Quartic;
  std::vector< std::vector< std::vector< double >>> Couplings_Higgs_Triple;
  std::vector< std::vector< std::vector< std::vector<double>>>> Couplings_Gauge_Higgs_22;
  std::vector< std::vector< std::vector< double >>> Couplings_Gauge_Higgs_21;
  std::vector< std::vector< std::vector< std::vector<std::complex<double>>>>> Couplings_Quark_Higgs_22;
  std::vector< std::vector< std::vector< std::complex<double> >>> Couplings_Quark_Higgs_21;
  std::vector< std::vector< std::vector< std::vector<std::complex<double>>>>> Couplings_Lepton_Higgs_22;
  std::vector< std::vector< std::vector< std::complex<double> >>> Couplings_Lepton_Higgs_21;


  std::vector<std::vector<std::vector<double>>> LambdaGauge_3;
  std::vector<std::vector<std::vector<double>>> LambdaHiggs_3;
  std::vector<std::vector<std::vector<double>>> LambdaHiggs_3_CT;
  std::vector< std::vector< std::vector< std::complex<double> >>> LambdaQuark_3;
  std::vector< std::vector< std::vector< std::complex<double> >>> LambdaLepton_3;
  std::vector< std::vector< std::vector< std::vector<std::complex<double> >>>> LambdaQuark_4;
  std::vector< std::vector< std::vector< std::vector<std::complex<double> >>>> LambdaLepton_4;

  std::vector< std::vector<double>> DebyeHiggs;
  std::vector< std::vector<double>> DebyeGauge;

  std::vector<double> ModifiedVEVVectorDim;

  std::vector<double> VevOrder;

  void initVectors();

  double VEff(const std::vector<double>& v,double Temp, int diff);
  double VTree(const std::vector<double>& v, int diff = 0);
  double CounterTerm(const std::vector<double>& v , int n);
  double V1Loop(const std::vector<double>& v, double Temp, int diff);
  /**
   * This function calculates the EW breaking VEV from all contributing field configurations.
   */
  double EWSBVEV(std::vector<double> v);


  /**
   * Reads the string linestr and sets the parameter point
   */
  virtual void ReadAndSet(const std::string& linestr, std::vector<double>& par) = 0;
  /**
   * Adds the name of the counterterms to the legend of the output file. This has to be specified in
   * the model file.
   */
  virtual std::string addLegendCT() = 0;
  /**
     * Adds the name of the VEVs, together with the critical VEV, the critical temperature and the ratio,
     * to the legend of the output file. This has to be specified in
     * the model file.
     */
  virtual std::string addLegendTemp() = 0;
  /**
     * Adds the name of the triple Higgs couplings at T=0 to the legend of the output file.
     * This has to be specified in
     * the model file.
     */
  virtual std::string addLegendTripleCouplings() = 0;
  /**
     * Adds the name of the VEVs to the legend of the output file. This has to be specified in
     * the model file.
     */
  virtual std::string addLegendVEV() = 0;


  /**
   * Reads the Lagrangian parameters from the vector 'par' and sets them to the model parameters. This also sets
   * the VEV configuration as well as the scale.
   * This has to be specified in the model file.
   */
  virtual void set_gen(const std::vector<double>& par) = 0;
  /**
   * Reads the counterterm parameters from the vector 'par' and sets them to the model parameters as well
   * as the Tensors \p Curvature_Higgs_CT_L1, \p Curvature_Higgs_CT_L2, \p Curvature_Higgs_CT_L3
   * , \p Curvature_Higgs_CT_L4 .
     * This has to be specified in the model file.
     */
  virtual void set_CT_Pot_Par(const std::vector<double>& par) = 0;
  /**
   * This will produce a terminal output of all the model parameters.
     * This has to be specified in the model file.
     */
  virtual void write() = 0;


  void set_All(const std::vector<double>& par,const std::vector<double>& parCT);

  /**
    * This will set all the tensors needed to describe the tree-level Lagrangian
    *  except for the counterterms in the potential.
      * This has to be specified in the model file.
      */
  virtual void SetCurvatureArrays() = 0;
  void CalculatePhysicalCouplings();
  void WeinbergFirstDerivative(std::vector<double>& res);
  void WeinbergSecondDerivative(std::vector<double>& res);
  void WeinbergThirdDerivative(std::vector<double>& res);
  void CalculateDebye();
  void CalculateDebyeGauge();
  void Prepare_Triple();

  /**
   * You can give the explicit Debye corrections to the Higgs mass matrix with this function if you know it.
   * Otherwise it is calculated via CalculateDebye(). If you know the corrections use this and let the function
   * return true, this will save you a lot of computing time.
   */
  virtual bool CalculateDebyeSimplified() = 0;

  /**
    * You can give the explicit Debye corrections to the gauge boson mass matrix with this function if you know it.
    * Otherwise it is calculated via CalculateDebyeGauge(). If you know the corrections use this and let the function
    * return true, this will save you a lot of computing time.
    */
   virtual bool CalculateDebyeGaugeSimplified() = 0;



  /**
   * You can give the explicit form of your tree-level potential here. This speeds up the computation time.
   */
  virtual double VTreeSimplified(const std::vector<double>& v) = 0;
  bool UseVTreeSimplified = false;
  /**
   * You can give the explicit form of your counterterm potential here. This speeds up the computation time.
   */
  virtual double VCounterSimplified(const std::vector<double>& v) = 0;
  bool UseVCounterSimplified = false;

  void HiggsMassesSquared(std::vector<double>& res, const std::vector<double>& v, double Temp = 0, int diff = 0);
  void GaugeMassesSquared(std::vector<double>& res, const std::vector<double>& v, double Temp = 0, int diff = 0);
  void QuarkMassesSquared(std::vector<double>& res, const std::vector<double>& v, int diff = 0);
  void LeptonMassesSquared(std::vector<double>& res,const std::vector<double>& v, int diff = 0);

  /**
   * Calculates the triple Higgs couplings at NLO in the mass basis.
   *
   * Use the vector TripleHiggsCorrectionsCWPhysical to save your couplings and set the nTripleCouplings
   * to the number of couplings you want as output.
   */
  virtual void TripleHiggsCouplings() = 0;
  double fbase(double MassSquaredA,double MassSquaredB);
  double fbaseTri(double MassSquaredA, double MassSquaredB, double MassSquaredC);
  /**
  * Calculates the counterterm parameters. Here you need to work out the scheme and implement the formulas.
  * This has to be specified in the model file.
  */
  virtual void calc_CT(std::vector<double>& par) = 0;


  double CWTerm(double MassSquared, double cb, int diff);
  double FCW(double MassSquared);
  double boson(double MassSquared, double Temp, double cb, int diff);
  double fermion(double Mass, double Temp, int diff);
  double Vl(double MassSquared, double Temp, int n, int diff =0 );
  double Vsf(double MassSquared, double Temp, int n, int diff = 0);
  double Vsb(double MassSquaredIn, double Temp, int n, int diff = 0);

  void resetbools();

  /**
      * This will convert the vector VEVminimizer with the VEVs used by the minimizer to a vector with the
      * complete VEV configuration VEVFunction used by the Class functions.
        * This has to be specified in the model file.
        */
  virtual void MinimizeOrderVEV(const std::vector<double>& VEVminimizer, std::vector<double>& VEVFunction) = 0;

  void FirstDerivativeOfEigenvalues(const Eigen::Ref<Eigen::MatrixXd> M,const Eigen::Ref<Eigen::MatrixXd> MDiff, std::vector<double> &res);
  void SecondDerivativeOfEigenvaluesNonRepeated(const Eigen::Ref<Eigen::MatrixXd> M,const Eigen::Ref<Eigen::MatrixXd> MDiffX,const Eigen::Ref<Eigen::MatrixXd> MDiffY,const Eigen::Ref<Eigen::MatrixXd> MDiffXY, std::vector<double> &res);


 /**
  * This function will check if the VEV at NLO is still close enough to the LO VEV. v has to be given in the configuration
  * of the minimizer.
  */
  bool CheckNLOVEV(const std::vector<double>& v);



  /**
   * This is a possible debugging function.
   */
  virtual void Debugging(const std::vector<double>& input, std::vector<double>& output)=0;

  /**
 * Checks if the tensors are correctly implemented. For this the fermion, quark and gauge boson masses are calculated and
 * printed next to the values defined in SMparah.h
 */
  void CheckImplementation(const std::vector<double>& par,const std::vector<double>& parCT);


};



